package Replit;

public class Calc {

    public int num1,num2;

    public Calc(int num1, int num2) {
        this.num1 = num1;
        this.num2 = num2;
    }

    public static int plus(int num1, int num2){

        return num1+num2;
    }


    public static int minus(int num1, int num2){

        return num1-num2;
    }
}
